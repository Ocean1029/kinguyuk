export type Lang = keyof typeof dictionaries;
export type Dict = (typeof dictionaries)["en"];
export type DictKey = keyof Dict;

export const LANGS: Lang[] = ["en", "zh"];
export const DEFAULT_LANG: Lang = "en";

export function isLang(value: string): value is Lang {
  return value === "en" || value === "zh";
}

export function getDictionary(lang: string): Dict {
  return isLang(lang) ? dictionaries[lang] : dictionaries.en;
}

/** Translator with English fallback, mirroring the original runtime behaviour. */
export type T = (key: DictKey) => string;

export function createTranslator(lang: string): T {
  const dict = getDictionary(lang);
  return (key) => dict[key] ?? dictionaries.en[key] ?? (key as string);
}
